import React, { Dispatch, useEffect, useState } from "react";
import { v4 as uuidv4} from 'uuid'
import { categories } from "../data/categories";
import { Activity } from "../types";
import { ActivityActions, ActivityState } from "../reducers/activity-reducer";

type FormProps = {
  dispatch: Dispatch<ActivityActions>,
  state : ActivityState
}

const initialForm : Activity = {
    id: uuidv4(),
    category: 1,
    name: "",
    calories: 0,
}

function Form({dispatch, state}: FormProps) {
  const [activity, setActivity] = useState<Activity>(initialForm);

  useEffect(() => {
    if(state.activeId) {
      const selectedActivity = state.activities.filter(stateActivity => stateActivity.id === state.activeId)[0]
      setActivity(selectedActivity)
    }
  }, [state.activeId, state.activities])

  // Tipamos el event e para el select y para el input
  const handleChange = (
    e:
      | React.ChangeEvent<HTMLSelectElement>
      | React.ChangeEvent<HTMLInputElement>
  ) => {
    const isNumberField = ["category", "calories"].includes(e.target.id);

    setActivity({
      // Copiamos nuestro State
      ...activity,
      // Modificamos el state
      // Mantenemos la integridad en el State para respetar los tipos y que no cambie el state a string
      [e.target.id]: isNumberField ? +e.target.value : e.target.value,
    });
  };

  const handleSubmit = (e: React.FormEvent<HTMLFormElement>) => {
    e.preventDefault();
    
    dispatch({type: "save-activity", payload:{newActivity: activity}})

    setActivity({
      ...initialForm,
      // Generamos un id nuevo a cada registro
      id: uuidv4()
    })
  };

  const isValidActivity = () => {
    const { name, calories } = activity;

    return name.trim() !== "" && calories > 0;
  };

  const placeholders: Record<number, string> = {
    1: "Ej. Hamburguesa, ensalada, arroz...",    
    2: "Ej. Correr, pesas, yoga..."             
  };
  

  return (
    <form
      className=" space-y-5 bg-white shadow p-10 rounded-lg"
      onSubmit={handleSubmit}
    >
      <div className=" grid grid-cols-1 gap-3">
        <label htmlFor="category">Categoria:</label>
        <select
          className=" border border-slate-300 p-2 rounded-lg w-full bg-white"
          id="category"
          value={activity.category}
          onChange={handleChange}
        >
          {categories.map((category) => (
            <option key={category.id} value={category.id}>
              {category.name}
            </option>
          ))}
        </select>
      </div>

      <div className=" grid grid-cols-1 gap-3">
        <label htmlFor="name" className=" font-bold">
          {" "}
          Actividad:
        </label>
        <input
          type="text"
          id="name"
          className=" border border-slate-300 p-2 rounded-lg"
          placeholder={placeholders[activity.category] || "Ej. Manzana, pizza, running, pesas..."}
          value={activity.name}
          onChange={handleChange}
        />
      </div>
      <div className=" grid grid-cols-1 gap-3">
        <label htmlFor="calories" className=" font-bold">
          {" "}
          Calorias:
        </label>
        <input
          type="number"
          id="calories"
          className=" border border-slate-300 p-2 rounded-lg"
          placeholder="Ej. 300 o 500"
          value={activity.calories}
          onChange={handleChange}
        />
      </div>

      <input
        type="submit"
        className=" bg-gray-800 hover:bg-gray-900 w-full p-2 font-bold text-white
         uppercase cursor-pointer disabled:opacity-10"
        value={activity.category === 1 ? "Guardar Comida" : "Guardar Ejercicio"}
        disabled={!isValidActivity()}
      />
    </form>
  );
}

export default Form;
